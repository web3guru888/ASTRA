class CognitiveMode(Enum):
    """Modes of cognitive processing."""
    PERCEPTION = "perception"           # Taking in information
    REASONING = "reasoning"             # Processing and inference
    REFLECTION = "reflection"           # Self-monitoring
    LEARNING = "learning"               # Updating knowledge
    GENERATION = "generation"           # Creating new insights
    VALIDATION = "validation"           # Testing and verification


@dataclass
class Perception:
    """A perceptual input from multi-modal data."""
    perception_id: str
    data: Any  # Could be numerical, image, spectrum, text
    data_type: str  # "numerical", "image", "spectrum", "text"
    features: Dict[str, Any]
    confidence: float
    timestamp: str = field(default_factory=lambda: datetime.now().isoformat())


@dataclass
class CognitiveInsight:
    """A scientific insight generated by the cognitive core."""
    insight_id: str
    insight_type: str  # "pattern", "causal", "theoretical", "anomaly"
    description: str
    confidence: float
    evidence: List[str]  # IDs of supporting evidence
    reasoning_chain: List[str]  # How was this derived?
    predictions: List[str]  # What does this predict?
    uncertainty: float  # Quantified uncertainty
    timestamp: str = field(default_factory=lambda: datetime.now().isoformat())


@dataclass
class ScientificDiscovery:
    """A complete scientific discovery."""
    discovery_id: str
    title: str
    discovery_type: str  # "empirical", "theoretical", "integrated"
    insights: List[str]  # IDs of insights
    theoretical_framework: Optional[str]  # Theory ID
    empirical_support: List[str]  # Evidence IDs
    confidence: float
    significance: float  # 0-1, scientific impact
    novelty: float  # 0-1, how novel?
    validity: float  # 0-1, how well validated?
    explanation: str  # Human-interpretable explanation
    next_steps: List[str]  # What to do next?
    timestamp: str = field(default_factory=lambda: datetime.now().isoformat())


class CognitiveCore:
    """
    Unified cognitive architecture for scientific discovery.

    This is the brain of ASTRA - orchestrating all cognitive capabilities.

    Architecture:
    ┌─────────────────────────────────────────────────────────────┐
    │                    COGNITIVE CORE                           │
    ├─────────────────────────────────────────────────────────────┤
    │                                                               │
    │  ┌─────────────┐  ┌──────────────┐  ┌──────────────┐       │
    │  │PERCEPTION   │→ │  REASONING   │→ │  REFLECTION  │       │
    │  │             │  │              │  │              │       │
    │  │Multi-Modal  │  │Neuro-Symbolic│  │Meta-Cognitive│       │
    │  │Understanding│  │Integration   │  │Monitoring    │       │
    │  └─────────────┘  └──────────────┘  └──────────────┘       │
    │          ↓                 ↓                 ↓              │
    │  ┌───────────────────────────────────────────────────┐    │
    │  │            DYNAMIC KNOWLEDGE GRAPH                 │    │
    │  │  (Semantic Brain - integrates all knowledge)      │    │
    │  └───────────────────────────────────────────────────┘    │
    │                                                               │
    │  Output: Scientific discoveries with explanations           │
    └─────────────────────────────────────────────────────────────┘
    """

    def __init__(self):
        # Core cognitive systems
        self.knowledge_graph = DynamicKnowledgeGraph()
        self.neuro_symbolic = NeuroSymbolicIntegrator()
        self.metacognition = MetaCognitiveArchitecture()

        # Existing ASTRA modules (if available)
        if ASTRA_MODULES_AVAILABLE:
            self.theory_validator = TheoryDataValidator()
            self.unified_discovery = UnifiedDiscoveryEngine()
            self.experiment_designer = ExperimentDesignEngine()

        # Cognitive state
        self.current_mode = CognitiveMode.PERCEPTION
        self.current_focus = None  # What is the system focused on?

        # Memory
        self.perceptions: List[Perception] = []
        self.insights: List[CognitiveInsight] = []
        self.discoveries: List[ScientificDiscovery] = []

        # Performance tracking
        self.discovery_count = 0
        self.insight_count = 0

    def perceive(self, data: Any, data_type: str, features: Dict[str, Any] = None
                ) -> Perception:
        """
        Take in perceptual information from multi-modal data.

        This is the entry point for all data into the cognitive system.
        """
        # Create perception
        perception_id = f"perception_{len(self.perceptions)}"
        perception = Perception(
            perception_id=perception_id,
            data=data,
            data_type=data_type,
            features=features or {},
            confidence=0.8  # Default confidence
        )

        self.perceptions.append(perception)

        # Start meta-cognitive monitoring
        trace_id = self.metacognition.start_reasoning_trace(
            f"Process {data_type} data",
            ReasoningTraceType.DATA_ANALYSIS
        )

        # Update cognitive state
        self.current_mode = CognitiveMode.PERCEPTION
        self.current_focus = perception_id

        return perception

    def reason(self, perception_id: str, reasoning_mode: CognitiveMode = CognitiveMode.REASONING
              ) -> List[CognitiveInsight]:
        """
        Reason about perceptions to generate insights.

        This is where the cognitive work happens - integrating perception,
        knowledge, reasoning, and self-monitoring.
        """
        if perception_id >= len(self.perceptions):
            return []

        perception = self.perceptions[perception_id]

        # Update cognitive state
        self.current_mode = reasoning_mode
        self.metacognition.current_state = CognitiveState.NORMAL

        # Start reasoning trace
        trace_id = self.metacognition.start_reasoning_trace(
            f"Reason about {perception.data_type} data",
            ReasoningTraceType.DATA_ANALYSIS
        )

        insights = []

        # 1. Neural pattern discovery
        if isinstance(perception.data, np.ndarray) and perception.data_type == "numerical":
            neural_disc, symbolic_form = self.neuro_symbolic.discover_and_formalize(
                perception.data,
                list(perception.features.keys()),
                ReasoningMode.INTEGRATED
            )

            # Create insights from neural discoveries
            for disc in neural_disc:
                insight_id = f"insight_{len(self.insights)}"
                insight = CognitiveInsight(
                    insight_id=insight_id,
                    insight_type=disc.pattern_type,
                    description=disc.pattern_description,
                    confidence=disc.confidence,
                    evidence=[perception_id],
                    reasoning_chain=[
                        f"Neural discovery: {disc.pattern_type}",
                        f"Features: {', '.join(disc.features)}",
                        f"Confidence: {disc.confidence:.2f}"
                    ],
                    predictions=[],
                    uncertainty=1.0 - disc.confidence
                )
                insights.append(insight)
                self.insights.append(insight)

                # Log to metacognition
                self.metacognition.add_reasoning_step(
                    trace_id=trace_id,
                    operation="neural_discovery",
                    inputs={"features": disc.features},
                    outputs={"pattern": disc.pattern_type},
                    confidence=disc.confidence,
                    reasoning_context=["Neural pattern detection"]
                )

            # Create insights from symbolic formalizations
            for form in symbolic_form:
                insight_id = f"insight_{len(self.insights)}"
                insight = CognitiveInsight(
                    insight_id=insight_id,
                    insight_type=form.formalization_type,
                    description=form.symbolic_form,
                    confidence=form.validation_score,
                    evidence=[perception_id],
                    reasoning_chain=[
                        f"Symbolic formalization: {form.formalization_type}",
                        f"Form: {form.symbolic_form}",
                        f"Validation: {form.validation_score:.2f}"
                    ],
                    predictions=[],
                    uncertainty=1.0 - form.validation_score
                )
                insights.append(insight)
                self.insights.append(insight)

        # 2. Knowledge graph reasoning
        # Find related entities in knowledge graph
        if perception.features:
            for feature_name, feature_value in perception.features.items():
                # Query knowledge graph for related concepts
                related = self.knowledge_graph.query(
                    query_type="entity",
                    name=feature_name,
                    min_confidence=0.5
                )

                if related:
                    # Create insight from knowledge graph
                    insight_id = f"insight_{len(self.insights)}"
                    insight = CognitiveInsight(
                        insight_id=insight_id,
                        insight_type="semantic",
                        description=f"Knowledge graph relation found for {feature_name}",
                        confidence=0.7,
                        evidence=[perception_id],
                        reasoning_chain=[
                            f"Query: {feature_name}",
                            f"Found {len(related)} related entities"
                        ],
                        predictions=[],
                        uncertainty=0.3
                    )
                    insights.append(insight)
                    self.insights.append(insight)

        # 3. End reasoning trace
        self.metacognition.end_reasoning_trace(
            trace_id=trace_id,
            outcome=f"Generated {len(insights)} insights",
            success=len(insights) > 0
        )

        return insights

