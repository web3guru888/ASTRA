    def discover(self, data: Any, data_type: str, features: Dict[str, Any]
                ) -> ScientificDiscovery:
        """
        Complete discovery pipeline: perceive → reason → learn → discover.

        This is the main entry point for scientific discovery.
        """
        # 1. Perceive
        perception = self.perceive(data, data_type, features)

        # 2. Reason
        insights = self.reason(perception.perception_id)

        # 3. Learn
        learned_entities = self.learn(insights)

        # 4. Generate discovery
        if insights:
            discovery_id = f"discovery_{self.discovery_count}"
            self.discovery_count += 1

            # Combine insights into coherent discovery
            high_confidence_insights = [i for i in insights if i.confidence > 0.6]

            if high_confidence_insights:
                discovery = ScientificDiscovery(
                    discovery_id=discovery_id,
                    title=f"Discovery from {data_type} analysis",
                    discovery_type="empirical",
                    insights=[i.insight_id for i in high_confidence_insights],
                    theoretical_framework=None,
                    empirical_support=[perception.perception_id],
                    confidence=np.mean([i.confidence for i in high_confidence_insights]),
                    significance=0.7,  # Simplified
                    novelty=0.6,  # Simplified
                    validity=np.mean([i.confidence for i in high_confidence_insights]),
                    explanation=self._generate_explanation(high_confidence_insights),
                    next_steps=self._generate_next_steps(high_confidence_insights)
                )

                self.discoveries.append(discovery)

                return discovery

        return None

    def _generate_explanation(self, insights: List[CognitiveInsight]) -> str:
        """Generate human-interpretable explanation from insights."""
        if not insights:
            return "No significant insights generated."

        explanation_parts = [
            f"Analysis generated {len(insights)} insights:",
        ]

        for insight in insights[:3]:  # Top 3
            explanation_parts.append(
                f"- {insight.description} (confidence: {insight.confidence:.2f})"
            )

        return " | ".join(explanation_parts)

    def _generate_next_steps(self, insights: List[CognitiveInsight]) -> List[str]:
        """Generate suggested next steps based on insights."""
        next_steps = []

        for insight in insights:
            if insight.insight_type == "correlation":
                next_steps.append(f"Investigate causal mechanism for {insight.description}")
            elif insight.insight_type == "cluster":
                next_steps.append(f"Analyze members of {insight.description} in detail")
            elif insight.insight_type == "anomaly":
                next_steps.append(f"Verify anomaly: {insight.description}")

        return next_steps[:5]  # Top 5

    def unify_theory_and_data(self, theory_description: str, data: np.ndarray
                             ) -> Dict[str, Any]:
        """
        Unify theoretical and empirical discovery.

        This integrates ASTRA's existing theory-data validation with
        the new cognitive architecture.
        """
        if not ASTRA_MODULES_AVAILABLE:
            return {"error": "ASTRA modules not available"}

        # 1. Create theory prediction
        theory = TheoryPrediction(
            theory_name=theory_description,
            prediction_type="qualitative",
            mathematical_form=theory_description,
            variables={},
            parameters={},
            confidence=0.7
        )

        # 2. Validate against data
        validation = self.theory_validator.validate_theoretical_prediction(
            theory, data, {}
        )

        # 3. Generate insights from validation
        insights = []

        insight = CognitiveInsight(
            insight_id=f"validation_{len(self.insights)}",
            insight_type="validation",
            description=f"Theory '{theory_description}' validation: {validation.status.value}",
            confidence=validation.agreement_score,
            evidence=[],
            reasoning_chain=[
                f"Theory: {theory_description}",
                f"Status: {validation.status.value}",
                f"Agreement: {validation.agreement_score:.2f}"
            ],
            predictions=[],
            uncertainty=1.0 - validation.agreement_score
        )
        insights.append(insight)
        self.insights.append(insight)

        return {
            'validation_result': validation,
            'insights': [i.insight_id for i in insights],
            'theory_supported': validation.status.value == "VALIDATED"
        }

    def design_experiments(self, n_proposals: int = 3) -> List[Dict]:
        """
        Design experiments to test current theories and fill knowledge gaps.

        Integrates ASTRA's existing experiment design with cognitive architecture.
        """
        if not ASTRA_MODULES_AVAILABLE:
            return []

        # Get knowledge gaps
        gaps = self.knowledge_graph.find_knowledge_gaps()

        if gaps:
            # Prioritize gaps
            high_priority_gaps = [g for g in gaps if g.priority > 0.7][:n_proposals]

            # Generate experiment proposals
            proposals = []

            for gap in high_priority_gaps:
                proposal = {
                    'gap_type': gap.gap_type,
                    'description': gap.description,
                    'priority': gap.priority,
                    'suggested_experiments': gap.suggestions
                }
                proposals.append(proposal)

            return proposals

        return []

    def get_cognitive_summary(self) -> Dict[str, Any]:
        """Get comprehensive summary of cognitive state."""
        return {
            'cognitive_mode': self.current_mode.value,
            'perceptions': len(self.perceptions),
            'insights': len(self.insights),
            'discoveries': len(self.discoveries),
            'knowledge_graph_stats': self.knowledge_graph.get_statistics(),
            'neuro_symbolic_stats': self.neuro_symbolic.get_performance_summary(),
            'metacognitive_report': self.metacognition.get_self_awareness_report()
        }

    def explain_discovery(self, discovery_id: str, audience_level: str = "expert"
                         ) -> Dict[str, str]:
        """
        Generate explanation for a discovery at different audience levels.

        This is key for explainable AI.
        """
        if discovery_id >= len(self.discoveries):
            return None

        discovery = self.discoveries[discovery_id]

        explanations = {
            "expert": self._explain_for_expert(discovery),
            "student": self._explain_for_student(discovery),
            "public": self._explain_for_public(discovery)
        }

        return {
            'audience_level': audience_level,
            'explanation': explanations.get(audience_level, explanations["expert"]),
            'all_explanations': explanations
        }

    def _explain_for_expert(self, discovery: ScientificDiscovery) -> str:
        """Generate expert-level explanation."""
        return (f"{discovery.title}. "
                f"Discovery type: {discovery.discovery_type}, "
                f"Confidence: {discovery.confidence:.2f}, "
                f"Significance: {discovery.significance:.2f}, "
                f"Novelty: {discovery.novelty:.2f}, "
                f"Validity: {discovery.validity:.2f}. "
                f"Details: {discovery.explanation}")

    def _explain_for_student(self, discovery: ScientificDiscovery) -> str:
        """Generate student-level explanation."""
        return (f"We discovered {discovery.title.lower()}. "
                f"This is {discovery.discovery_type} evidence with "
                f"{discovery.confidence:.0%} confidence. "
                f"The significance is {discovery.significance:.0%} and "
                f"the novelty is {discovery.novelty:.0%}.")

    def _explain_for_public(self, discovery: ScientificDiscovery) -> str:
        """Generate public-level explanation."""
        return (f"ASTRA discovered {discovery.title.lower()}. "
                f"This finding has high scientific importance "
                f"and helps us understand the universe better.")


